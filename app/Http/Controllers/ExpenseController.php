class ExpenseController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $expenses = Expense::orderBy('name','asc')->with('outlet','category')->get();
        return view('expense.all_expense',compact('expenses'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $outlets = Outlet::all();
        $expenseCategory = ExpenseCategory::all();

        return view('expense.add_expense',compact('outlets','expenseCategory'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $expense = new Expense();
        $expense->outlet_id	= $request->outlet_id;
        $expense->expense_category_id = $request->expense_category_id;
        $expense->name = $request->name;
        $expense->amount = $request->amount;
        $expense->remark = $request->remark;
        $expense->save();

        return redirect()->route('expenses.index');
    }

